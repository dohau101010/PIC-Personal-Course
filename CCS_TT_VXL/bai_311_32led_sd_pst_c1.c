#include <tv_kit_vdk_pic_all.c>

const unsigned int8 dlsd[37]=
{  0b00000000, 0b00000001, 0b00000100, 0b00000100, 0b00001000, 0b00010000, 0b00100000, 0b01000000, 0b10000000,
   0b10000001, 0b10000010, 0b10000100, 0b10001000, 0b10010000, 0b10100000, 0b11000000,
   0b11000001, 0b11000010, 0b11000100, 0b11001000, 0b11010000, 0b11100000,
   0b11100001, 0b11100010, 0b11100100, 0b11101000, 0b11110000,
   0b11110001, 0b11110010, 0b11110100, 0b11111000,
   0b11111001, 0b11111010, 0b11111000,
   0b11111101, 0b11111110,
   0b11111111 };
void b311_8led_sd_pst_c1(usi16 dl)
{
   usi8 n;
   for(n=0;n<37;n++)
   {
      xuat_32led_don_4byte(0,0,0,dlsd[n]);
      delay_ms(dl);
   }
}

void main()
{
   set_up_port();
   while(true)
   {
      b311_8led_sd_pst_c1(100);
   }
}
   

